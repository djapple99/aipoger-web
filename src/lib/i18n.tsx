export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('zh');
  const [initialized, setInitialized] = useState(false);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    if (typeof window !== 'undefined') {
      localStorage.setItem('aipoger_lang', l);
      const url = new URL(window.location.href);
      url.searchParams.set('lang', l);
      window.history.replaceState(null, '', url.toString());
    }
  }, []);

  const toggleLang = useCallback(() => {
    setLang(lang === 'zh' ? 'en' : 'zh');
  }, [lang, setLang]);

  useEffect(() => {
    if (initialized) return;
    if (typeof window === 'undefined') return;
    const fromUrl = new URLSearchParams(window.location.search).get('lang');
    if (fromUrl === 'en' || fromUrl === 'zh') {
      setLangState(fromUrl);
      localStorage.setItem('aipoger_lang', fromUrl);
      setInitialized(true);
      return;
    }
    const stored = localStorage.getItem('aipoger_lang');
    if (stored === 'en' || stored === 'zh') {
      setLangState(stored);
    }
    setInitialized(true);
  }, [initialized]);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>): string => {
      let s = dict[lang][key] ?? key;
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          s = s.replaceAll(`{{${k}}}`, String(v));
        }
      }
      return s;
    },
    [lang],
  );

  // Sync lang to URL param so all pages can read it
  useEffect(() => {
    if (typeof window === 'undefined') return;
    const url = new URL(window.location.href);
    const fromUrl = url.searchParams.get('lang');
    if ((fromUrl === 'en' || fromUrl === 'zh') && fromUrl !== lang) return;
    url.searchParams.set('lang', lang);
    window.history.replaceState(null, '', url.toString());
  }, [lang]);

  useEffect(() => {
    if (typeof document === 'undefined') return;
    document.documentElement.lang = lang === 'zh' ? 'zh-Hant' : 'en';
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, setLang, toggleLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}
